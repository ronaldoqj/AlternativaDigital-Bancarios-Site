 <br><br>
		 A Fetrafi/RS exerce um papel fundamental na organização e encaminhamento de questões coletivas e específicas dos trabalhadores do setor. Além de lutar por melhores condições de trabalho e uma remuneração mais justa, de acordo com os lucros obtidos pelas instituições, a entidade agrega discussões permamentes e desenvolve ações em prol da saúde dos trabalhadores; da isonomia de tratamento; da manutenção dos bancos públicos e da regulamentação do sistema financeiro.
<br><br>
Um dos principais desafios assumidos pela diretoria da Federação é preparar a categoria para a Campanha Salarial. A organização da campanha inicia com os encontros estaduais de funcionários do Banco do Brasil e de empregados da Caixa, onde são debatidos os temas específicos destes segmentos. Já o maior fórum deliberativo da Campanha Salarial no Estado é a Conferência Estadual dos Trabalhadores e Trabalhadoras em Instituições Financeiras do RS.
